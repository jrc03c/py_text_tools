def find_index(fn, x):
    for i, v in enumerate(x):
        if fn(v):
            return i

    return -1


def replace_all(text, a, b):
    assert type(text) is str, (
        "The first value passed into the `replace_all` function must be a string!"
    )

    assert type(a) is str, (
        "The second value passed into the `replace_all` function must be a string!"
    )

    assert type(b) is str, (
        "The third value passed into the `replace_all` function must be a string!"
    )

    return b.join(text.split(a))


def string_to_array(x):
    assert type(x) is str, (
        "The value passed into the `string_to_array` function must be a string!"
    )

    out = []

    for i in range(0, len(x)):
        out.append(x[i])

    return out


def strip(text):
    assert type(text) is str, (
        "The value passed into the `strip` function must be a string!"
    )

    out = ""

    for i in range(0, len(text)):
        char = text[i].lower()

        if char in punctuation:
            out += single_space

        else:
            out += char

    while double_space in out:
        out = replace_all(out, double_space, single_space)

    return out.strip()
